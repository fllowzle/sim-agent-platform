# ---- COMSOL .mph Extractor (requires mph library) ----

class ComsolMphExtractor:
    SOFTWARE = 'COMSOL Multiphysics'

    def extract(self, mph_path):
        e = ExtractedModel(software=self.SOFTWARE, file_path=str(mph_path), template_name=Path(mph_path).stem)
        try:
            import mph
        except ImportError:
            e.raw_notes.append('mph library not installed: pip install mph')
            e.extraction_confidence = 'low'
            return e
        try:
            client = mph.Client()
            model = client.load(str(mph_path.absolute()))
            try:
                e.physics_interfaces = model.physics()
                for pn in e.physics_interfaces:
                    pl = pn.lower()
                    if any(k in pl for k in ['ewfd', 'electromagnetic', 'wave']):
                        e.physics_type = 'electromagnetic_waves'
                    elif any(k in pl for k in ['ht', 'heat']):
                        e.physics_type = 'heat_transfer'
                    elif any(k in pl for k in ['solid', 'struct']):
                        e.physics_type = 'solid_mechanics'
            except: pass
            try:
                params = model.parameters()
                for k, v in params.items():
                    e.geometry_params[k] = str(v)
            except: pass
            try:
                studies = model.studies()
                if studies:
                    sn = studies[0].lower()
                    if 'eigen' in sn: e.study_type = 'eigenfrequency'
                    elif 'station' in sn or 'static' in sn: e.study_type = 'stationary'
                    elif 'time' in sn or 'transient' in sn: e.study_type = 'time_dependent'
            except: pass
            try:
                e.plots = list(model.plots())
            except: pass
            e.extraction_confidence = 'high' if e.physics_interfaces else 'medium'
            client.clear()
        except Exception as ex:
            e.raw_notes.append('Extraction failed: ' + str(ex))
            e.extraction_confidence = 'low'
        return e


# Register COMSOL mph extractor in ModelLearner
ModelLearner.__init__ = lambda self: setattr(self, '_ext', {'.py': ComsolScriptExtractor(), '.mph': ComsolMphExtractor()})
ModelLearner.register = lambda self, ext, ex: self._ext.__setitem__(ext.lower(), ex)
ModelLearner.learn_from = ModelLearner.learn_from
ModelLearner.supported_formats = ModelLearner.supported_formats
